    private void jButton_SAVEActionPerformed(java.awt.event.ActionEvent evt) {                                             
        int row ;
        row = ProfessorTable.getSelectedRow();
        if(row == -1){
            Profesori p;
            p = new Profesori();
            
            p.setProfesoriId(Integer.parseInt(Profesori_Id.getText()));
            p.setProfesoriEmri(Profesori_Emri.getText());
            p.setProfesoriMbiemri(Profesori_Mbiemri.getText());
            p.setProfesoriTelefoni( Telefoni_Profesori.getText());
            p.setProfesoriMateriali(Materiali.getText());
            p.setProfesoriDataLindjes(jDateChooser_DataLindjes.getDateFormatString());
            
           

            try {
                pi.create(p);
            } catch (BDexception ex) {
                Logger.getLogger(ProfesoriForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            tableLoad();
            clearInputs();
        }
        else{
            Profesori p;
            p = new Profesori ();
           
            p.setProfesoriId(Integer.parseInt(Profesori_Id.getText()));
            p.setProfesoriEmri(Profesori_Emri.getText());
            p.setProfesoriMbiemri(Profesori_Mbiemri.getText());
            p.setProfesoriTelefoni( Telefoni_Profesori.getText());
            p.setProfesoriMateriali(Materiali.getText());
            p.setProfesoriDataLindjes(jDateChooser_DataLindjes.getDateFormatString());
            
           
            try {
                pi.create(p);
            } catch (BDexception ex) {
                Logger.getLogger(ProfesoriForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            tableLoad();
            clearInputs();
        }

    }                                            

    private void jButton_DELETEActionPerformed(java.awt.event.ActionEvent evt) {                                               
        int row ;
        row = ProfessorTable.getSelectedRow();
        if (row != -1){
            Object [] ob = {"Po", "Jo"};
            int result = JOptionPane.showOptionDialog(this," A deshironi ta fshini?", "Fshirja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);

            if (result == 0){
                Profesori p;
                p = ptm.getProfesori(row);
                try {
                    pi.remove(p);
                } catch (BDexception se) {
                    JOptionPane.showMessageDialog(this, se.getMessage());
                }
                tableLoad();
            }
            clearInputs();

        } else {
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");

        }
    }                                              


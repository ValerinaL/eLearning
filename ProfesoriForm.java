    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = ProfessorTable.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {


            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                  Profesori p;
                    p = ptm.getProfesori (selectedIndex);
                   
        Profesori_Id.setText(p.getProfesoriId() + "");
        Profesori_Emri.setText(p.getProfesoriEmri() + "");
        Profesori_Mbiemri.setText(p.getProfesoriMbiemri() + "");
        jDateChooser_DataLindjes.setDateFormatString(p.getProfesoriDataLindjes());
        Telefoni_Profesori.setText(p.getProfesoriTelefoni() + "");
        Materiali.setText(p.getProfesoriMateriali() + "");
        
                
         }
            }
      });
    }
    
    
    public void tableLoad() {
        try {
            List<Profesori> list;
            list = pi.findAll();
            ptm = new ProfesoriTableModel(list);
             ProfessorTable.setModel(ptm);
            ptm.fireTableDataChanged();        } catch (BDexception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
